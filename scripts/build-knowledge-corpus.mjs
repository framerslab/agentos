/**
 * Reads the capability-catalog.json generated by the extensions registry
 * build script and converts each entry into a corpus chunk.
 *
 * @returns {Array<{id: string, heading: string, content: string, category: string}>}
 */
function loadCapabilityCatalog() {
  const catalogPath = resolve(
    PKG_ROOT,
    '../agentos-extensions-registry/src/capability-catalog.json',
  );

  if (!existsSync(catalogPath)) {
    console.warn('[build-knowledge-corpus] capability-catalog.json not found, skipping tool reference source');
    return [];
  }

  /** @type {Array<{id: string, name: string, description: string, category: string, tools: string[], requiredSecrets: string[]}>} */
  const catalog = JSON.parse(readFileSync(catalogPath, 'utf-8'));

  return catalog.map((entry) => ({
    id: `tool-ref:${entry.id}`,
    heading: entry.name,
    content: [
      entry.description,
      entry.tools.length > 0 ? `Tools: ${entry.tools.join(', ')}.` : '',
      `Required secrets: ${entry.requiredSecrets.length > 0 ? entry.requiredSecrets.join(', ') : 'none'}.`,
    ]
      .filter(Boolean)
      .join(' '),
    category: 'tools',
  }));
}

// ============================================================================
// Source 2: Skills summaries
// ============================================================================

/**
 * Reads all SKILL.md files from the curated skills registry, parses YAML
 * frontmatter and the first body paragraph to build summary chunks.
 *
 * Uses a lightweight frontmatter parser (splits on `---` delimiters) so the
 * build script has zero external dependencies beyond Node built-ins.
 *
 * @returns {Array<{id: string, heading: string, content: string, category: string}>}
 */
function loadSkillsSummaries() {
  const curatedDir = resolve(PKG_ROOT, '../agentos-skills/registry/curated');

  if (!existsSync(curatedDir)) {
    console.warn('[build-knowledge-corpus] curated skills directory not found, skipping skills source');
    return [];
  }

  const skillDirs = readdirSync(curatedDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  const chunks = [];

  for (const dir of skillDirs) {
    const skillPath = join(curatedDir, dir, 'SKILL.md');
    if (!existsSync(skillPath)) continue;

    const raw = readFileSync(skillPath, 'utf-8');
    const { frontmatter, body } = parseFrontmatter(raw);
    const firstParagraph = extractFirstParagraph(body);
    const name = frontmatter.name || dir;
    const description = frontmatter.description || firstParagraph;

    if (!name && !description && !firstParagraph) continue;

    chunks.push({
      id: `skill:${dir}`,
      heading: `${humanizeIdentifier(name)} Skill`,
      content: description === firstParagraph ? description : [description, firstParagraph].filter(Boolean).join('. '),
      category: 'skills',
    });
  }

  return chunks;
}

/**
 * Lightweight YAML-frontmatter parser that does not require the `gray-matter`
 * dependency. Splits on `---` delimiters and extracts key-value pairs from
 * the YAML block using simple line parsing.
 *
 * @param {string} raw - Raw markdown file content.
 * @returns {{ frontmatter: Record<string, string>, body: string }}
 */
function parseFrontmatter(raw) {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return { frontmatter: {}, body: raw };

  const yamlBlock = match[1];
  const body = raw.slice(match[0].length).trim();

  /** @type {Record<string, string>} */
  const frontmatter = {};
  for (const line of yamlBlock.split('\n')) {
    const colonIndex = line.indexOf(':');
    if (colonIndex === -1) continue;
    const key = line.slice(0, colonIndex).trim();
    let value = line.slice(colonIndex + 1).trim();
    // Strip surrounding quotes
    if ((value.startsWith("'") && value.endsWith("'")) || (value.startsWith('"') && value.endsWith('"'))) {
      value = value.slice(1, -1);
    }
    frontmatter[key] = value;
  }

  return { frontmatter, body };
}

/**
 * Extracts the first non-empty, non-heading paragraph from a markdown body.
 *
 * @param {string} body - Markdown body after frontmatter.
 * @returns {string} The first paragraph text, or empty string.
 */
function extractFirstParagraph(body) {
  const lines = body.split('\n');
  const paragraph = [];
  let inParagraph = false;

  for (const line of lines) {
    const trimmed = line.trim();

    // Skip headings and empty lines before the first paragraph
    if (!inParagraph) {
      if (trimmed === '' || trimmed.startsWith('#')) continue;
      inParagraph = true;
    }

    // End paragraph on blank line
    if (inParagraph && trimmed === '') break;

    paragraph.push(trimmed);
  }

  return paragraph.join(' ');
}

/**
 * Convert a kebab-case or snake_case identifier into a human-readable label.
 *
 * @param {string} value - Raw identifier.
 * @returns {string} Human-readable label.
 */
function humanizeIdentifier(value) {
  return value
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/\b\w/g, (match) => match.toUpperCase());
}

