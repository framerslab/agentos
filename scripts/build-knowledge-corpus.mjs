// ============================================================================
// Main — assemble and write corpus
// ============================================================================

function main() {
  console.log('[build-knowledge-corpus] Assembling platform knowledge corpus...');

  const corpus = [];

  // Source 1: Capability catalog
  const tools = loadCapabilityCatalog();
  console.log(`  [1/5] Capability catalog: ${tools.length} entries`);
  corpus.push(...tools);

  // Source 2: Skills summaries
  const skills = loadSkillsSummaries();
  console.log(`  [2/5] Skills summaries: ${skills.length} entries`);
  corpus.push(...skills);

  // Source 3: FAQ
  const faq = getFaqEntries(skills.length);
  console.log(`  [3/5] FAQ entries: ${faq.length} entries`);
  corpus.push(...faq);

  // Source 4: API quick reference
  const api = getApiReferenceEntries();
  console.log(`  [4/5] API reference: ${api.length} entries`);
  corpus.push(...api);

  // Source 5: Troubleshooting
  const troubleshooting = getTroubleshootingEntries();
  console.log(`  [5/5] Troubleshooting: ${troubleshooting.length} entries`);
  corpus.push(...troubleshooting);

  // Write the combined corpus
  const outputDir = join(PKG_ROOT, 'knowledge');
  if (!existsSync(outputDir)) {
    mkdirSync(outputDir, { recursive: true });
  }

  const outputPath = join(outputDir, 'platform-corpus.json');
  writeFileSync(outputPath, JSON.stringify(corpus, null, 2) + '\n', 'utf-8');

  const sizeBytes = readFileSync(outputPath).length;
  console.log(
    `\n[build-knowledge-corpus] Done. ${corpus.length} entries, ${sizeBytes.toLocaleString()} bytes -> ${outputPath}`,
  );
}

main();
